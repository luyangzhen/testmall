<template>
  <swiper>
      <swiper-item v-for="item in banners">
        <a :href="item.link">
          <img :src="item.image" alt="" @load="imageLoad">
        </a>
      </swiper-item>
    </swiper>
</template>

<script>
import {Swiper, SwiperItem} from 'components/common/swiper'
export default {
    props: {
        banners: {
            type: Array,
            default() {
                return []
            }
        }
    },
name: 'HomeSwiper',
data() {
    return {
        isLoad: false
    }
},
components: {
    Swiper,
    SwiperItem
},
methods: {
    imageLoad() {
        if (!this.isLoad) {
            this.$emit('swiperImageLoad')
            this.isLoad = true
        }
    }
}
}
</script>

